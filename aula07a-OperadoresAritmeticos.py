n1 = int(input('Um valor: '))
n2 = int(input('Outro valor: '))
print('Soma vale {}'.format(n1 + n2))
print('Subtração vale {}'.format(n1 - n2))
print('Multiplicação vale {}'.format(n1 * n2))
print('Divisão vale {:.3f}'.format(n1 / n2))
print('Potência vale {}'.format(n1 ** n2))
print('Divisão inteira vale {}'.format(n1 // n2))
print('Resto divisão vale {}'.format(n1 % n2))
